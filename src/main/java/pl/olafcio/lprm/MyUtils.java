package pl.olafcio.lprm;

public final class MyUtils {
    public MyUtils() {
    }

    public static String smallCaps(String text) {
        return text.replace("q", "ᴏ̨").replace("w", "ᴡ").replace("e", "ᴇ").replace("r", "ʀ").replace("t", "ᴛ").replace("y", "ʏ").replace("u", "ᴜ").replace("i", "ɪ").replace("o", "ᴏ").replace("p", "ᴘ").replace("a", "ᴀ").replace("d", "ᴅ").replace("f", "ғ").replace("g", "ɢ").replace("h", "ʜ").replace("j", "ᴊ").replace("k", "ᴋ").replace("l", "ʟ").replace("z", "ᴢ").replace("c", "ᴄ").replace("v", "ᴠ").replace("b", "ʙ").replace("n", "ɴ").replace("m", "ᴍ");
    }
}
